<p class="body1">This relatively common profile includes two factors that appear
in some ways to be contradictory. On the one hand, Influence
is the factor of excitement, enjoyment and extrovert
impulsiveness. On the other, Compliance is related to
precision, detail and carefully-followed rules. The ways in
which this apparent contradiction is resolved form the
backbone of the interpretation for this behavioural type.

 </p>
<p class="body1">The differences in approach between these two factors are
resolved in an unusual approach. Normally, two or more high
DISC factors will tend to reinforce each other's common points,
and blend to make up the entire behavioural approach. This is
not the case with the two opposing styles of Influence and
Compliance - what we see instead, with this type, is one factor (Influence) appearing in relaxed, open and favourable situations, while in more formal or structured circumstances, the other (Compliance) will come to the fore.

