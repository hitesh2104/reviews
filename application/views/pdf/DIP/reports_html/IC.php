<h1>Relating to Others</h1>
<p>The ways in which this style will relate to other people is highly dependent on the circumstances under which an encounter takes place. In a circle of friends, or in a 'party' atmosphere, this style is capable of quite confident and extrovert behaviour. In a formal work environment, or pressurised atmosphere, such as an interview for a job, this confidence will apparently evaporate, and the style will fall into line with its more Compliant aspect.
</p>

