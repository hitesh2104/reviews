<h1>Relating to Others</h1>
<p>Influence and Steadiness are both social factors, suggesting that this type will interact easily and skilfully with other people, and that they possess the personal self-confidence to mix relatively easily with strangers, or in unusual situations. They do have a strong sense of independence, however, and are prepared to go to considerable lengths to maintain their own sense of identity, and to protect and defend their own viewpoint.</p>

