<h1>Motivating Factors</h1>

<table>
  <tr>
  <td width="250" align="right"><img src="http://assessmenthouse.com/reports_html/images/moving.jpg"  alt="" style="float:left; padding-left:15px; padding-top:15px;" /></td>
    <td width="390" ><p class="body1">Highly Influential individuals are motivated by relations with others. Specifically, they need to feel accepted by those around them, and react badly if they perceive themselves to be rejected or disliked. Praise and approval make a strong impression on them, and they will sometimes go to great lengths to achieve this kind of reaction from other people.</p>
<p class="body1">Especially important to this type of person are the opinions and reactions of their particularly close friends. When a High-I develops very close ties with somebody, that person becomes part of their 'Influence Group', as it is known. Their actions will often be designed to improve and extend relations within this group, even to the extent of alienating people who are not part of this circle. This factor can make highly Influential characters appear unpredictable at times.</p>
