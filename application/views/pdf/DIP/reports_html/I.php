<h1>Relating to Others</h1>
<p>Relating to others is what a High-I (highly Influential person) do best. They are open to others and confident in their own social abilities, allowing them to interact positively in almost any situation. Their strong and evident confidence, coupled with their genuine interest in the ideas and especially feelings of other people, are often found charming by those around them.</p>


