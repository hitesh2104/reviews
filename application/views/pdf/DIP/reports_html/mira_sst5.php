<br/><br/>
<p>The Matrix of Intellectual Reasoning Assessment (MIRA)is a non-verbal mental ability test that requires test-takers to find solutions to a range of different problems. It measures observation skills, thinking ability, intellectual capacity and efficiency. It also measures one's ability to formulate new concepts, extract meaning out of ambiguity and to think clearly about complex situations and events.</p>
<br/><br/>
<p>The Stratified Systems Theory (SST) developed by Eliot Jaquesdefines work in different organisationallevels, mainly on the basis of decision-making complexity. MIRA measures the individual's ability to find solutions through different levels of complex problems.</p>

