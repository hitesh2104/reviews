    <img src="http://assessmenthouse.com/pdf_gen/reports_html/images/sst1.png" style="text-align: center;"/>
    <p>The candidate seems to be more comfortable working in an environment with tangible outputs where quality is their main priority. They may prefer to follow a clear set of guidelines to perform optimally and are more inclined to function in an operational and/or technical environment. A structured environment will probably suite the candidate better where they can plan and organise their daily activities. The candidate might also be more inclined to follow routines as this will make their work activities more predictable and measurable.</p>
