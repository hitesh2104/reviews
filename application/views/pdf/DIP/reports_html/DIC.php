<h1>Relating to Others</h1>
The ways in which this type relates to other people will tend to vary according to their social situation, depending on the relative formality of their surroundings. In more social, casual circumstances, they will project a friendly and animated style, being open and enthusiastic in general style.<BR><BR>
If their situation is more formal or closely regulated, however, a more direct and determined side will develop, being both assertive and self-controlled, and showing far less of the sociable, gregarious side associated with favourable situations. It is in formal situations of this kind that the more ambitious and driving elements will come to the fore, and it is likely that the individual will also adopt a somewhat plain-speaking, blunt aspect.
