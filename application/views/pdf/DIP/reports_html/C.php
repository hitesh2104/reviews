<h1>Relating to Others</h1>
<p>
Highly Compliant types have many strengths, but the ability to relate easily to other people is rarely among these. The combination of a reticent social style with a certain innate suspiciousness makes it difficult for this type of person to form or maintain close relationships, and this is especially true in a business sense. Their friendships or close acquaintances will normally be based on mutual interests or common aims, rather than emotional considerations.</p>

