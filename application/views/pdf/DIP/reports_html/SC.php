<h1>Relating to Others</h1>
<p>This rather hesitant style often finds it difficult to relate to other people, especially in unfamiliar settings, because they need to know exactly where they stand before they feel able to act. While they value friendships and strong relations with others, this factor is often disguised by an apparently aloof and reserved style. In order to interact effectively with others, this type will look to more direct and outgoing styles to initiate and take control of interpersonal issues.</p>


