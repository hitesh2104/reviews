<h1>Relating to Others</h1>
<p>Influence, Steadiness and Compliance, the three important factors in this profile, all confer certain communicative strengths on this type. In combination, these three elements give people of this kind a number of strengths in the field of relations with other people. Influence is the factor that relates to an outgoing, friendly style, while Steadiness confers capable listening skills and patience with others. Finally, Compliance gives a rational aspect to such an individual, helping them to present cogent and coherent arguments when necessary.</div>

