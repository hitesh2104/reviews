<h1>Relating to Others</h1>
<p>Of all DISC profiles, those following this general pattern are probably the most effective at relating to other people, in an all round sense. They are able to socialise easily and their gregarious natures allow them to feel at ease with people they do not know. They are often persuasive and charming, but the Steadiness in this profile means that they are also able to adopt a more open, relaxed approach when a situation demands, becoming less directly active and more receptive to the ideas and feelings of other people.<p></div>

