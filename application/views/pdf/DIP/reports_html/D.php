<p class="body1">The High-D profile is often described as the 'Autocrat', and for
good reason. Dominance is the factor of control and
assertiveness, and with no other high factors in the profile to
balance this, the pure High-D can be remarkably domineering,
and even overbearing at times. This type of person has a very
high need to achieve, and because of this they are often
ambitious and competitive, striving aggressively to achieve their
goals. They are dynamic and adaptable, and show decisiveness
and a capacity for direct leadership.
 </p><h1>Relating to Others</h1>
<p>
The emphasis that this type of person places on achievement and success significantly affects their relations with other people. In extreme cases, a High-D can come to treat other people simply as a means to an end, or a way of achieving their personal goals. Dominance is not an emotional factor, and individuals with this type of profile will tend not to place great importance on feelings, either their own or others'. The competitive side of Dominance can lead this type of person to see challenges and opposition everywhere, and others sometimes find it difficult to break through this naturally suspicious, sceptical shell.</p><br /></td>
