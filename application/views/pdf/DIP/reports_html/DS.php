 <h1>Relating to Others</h1>
<p>The unusual combination of factors represented by this style make it difficult to predict their likely approach to other people. On one hand, Dominance is a controlled and suspicious factor, preferring to avoid revealing information to others, but on the other Steadiness is an open characteristic, representing a person who likes to maintain amiable and trusting relations with those around them. This suggests that a person showing both high Dominance and Steadiness will adapt their social style to a particular situation, showing a friendlier side to their character if they feel that they can trust the people around them.</p></td>
