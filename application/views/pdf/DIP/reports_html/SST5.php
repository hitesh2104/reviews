<h1>SST LEVEL 5 – STRATEGY CONCEPTUALISING</h1>
<img src="/images/cap_light.jpg" />
<p>Conceptualising strategies for future industry viability is a key component for individuals working on this level. It seems that they have the capability to deal with the complexity of this environment, thus working with information that is very vague and/or mostly unavailable. They probably find it stimulating to work in environments that offer little if any structure at all. Working and thinking about the macro economy and making decisions that can impact the whole organisation will probably stimulate them.</p>
<p>Leaders on this level usually manage numerous business units and at times probably different organisations. They can think across different systems and/or units, hence being good CEO’s of large organisations.s</p>
