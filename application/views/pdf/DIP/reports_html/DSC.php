<h1>Relating to Others</h1>
<p>The low Influence score in this profile shape indicates that relating to other people is not an area of particular emphasis for an individual of this type. Where they do respond to others on more than a purely practical basis, they will tend to react to comments or suggestions from other parties rather than offering direct input themselves. As a situation becomes more difficult, their willingness to make direct input will increase dramatically, but their readiness to communicate on a personal level will reduce proportionately.</p>
